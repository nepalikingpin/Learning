c8=mod(floor(cd/128),2);
%combining image
cc=2*(2*(2*(2*(2*(2*(2*(2*c8+c7)+c6)+c5)+c4)+c3)+c2)+c1);
subplot(2,5,1);
imshow(c);
title('original image');
