clc;
c=imread('D:\Matlab Lab/6.jpg');
%extracting image information in code
cd =double(c);
%extracting all bit one by one
c1=mod(cd,2);
c2 = mod(floor(cd/2),2);
c3 = mod(floor(cd/4),2);
c4=mod(floor(cd/8),2);
c5=mod(floor(cd/16),2);
